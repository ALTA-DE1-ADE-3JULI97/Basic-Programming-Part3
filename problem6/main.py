def is_prime(n):
    if n < 2:
        return False
    for i in range(2, int(n ** 0.5) + 1):
        if n % i == 0:
            return False
    return True
def full_prima(N):
    if not is_prime(N):
        return False
    str_N = str(N)
    for i in range(len(str_N)):
        if not is_prime(int(str_N[i])):
            return False
    return True
